/**
 * Сокращает полный адрес Яндекс-геокодера до формата
 * «Город, Улица, Дом» (без страны и региона).
 *
 * Примеры:
 *  «Россия, Краснодарский край, Краснодар, ул. Красная, 5» → «Краснодар, ул. Красная, 5»
 *  «Россия, Москва, Арбат, 1»                              → «Москва, Арбат, 1»
 */
export function shortenAddress(full: string): string {
  if (!full) return full

  const SKIP = [
    'Россия', 'Russia',
    // Распространённые регионы — при необходимости дополняй
    'Краснодарский край', 'Московская область', 'Ленинградская область',
    'Ростовская область', 'Ставропольский край', 'Свердловская область',
    'Новосибирская область', 'Тюменская область', 'Татарстан',
    'Республика Татарстан', 'Башкортостан', 'Республика Башкортостан',
  ]

  const parts = full
    .split(',')
    .map(p => p.trim())
    .filter(p => p.length > 0 && !SKIP.includes(p))

  // Оставляем максимум 3 части (город, улица, дом)
  return parts.slice(-3).join(', ')
}
